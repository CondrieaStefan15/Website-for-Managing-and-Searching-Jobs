package com.psbd.psbd;

public class Domenii {
	
	private int id_domeniu;
	private String numeDomeniu;
	
	public void setIdDomeniu(int id_domeniu){
		this.id_domeniu = id_domeniu;
	}
	public void setNumeDomeniu(String numeDomeniu){
		this.numeDomeniu = numeDomeniu;
	}
	
	
	public int getIdDomeniu(){
		return this.id_domeniu;
	}
	public String getNumeDomeniu(){
		return this.numeDomeniu;
	}

}
